// 测试代码
import cronToChinese from "./dist/index.js";
function testCronExpression() {
  const testCases = [
    // 测试只有*和?的情况
    "* * * * *",
    "* * * * * *",
    "* * * * ?",
    "* * * ? * *",
    // 测试年
    "0 0 2 1 * ? *",
    "0 0 2 1 * ? 2021",
    // 测试月
    "* * * * 1-10 ? *",
    "* * * * 4,12 ? *",
    "* * * * Apr,JUN ? 2024",
    "* * * * 4,12 ? 2024,2026",
    // 测试周
    "* * * * * 2",
    "* * * * 10-11 2 2012",
    "* * * * * 1-3 2012",
    "* * * * 12 Mon-WeD 2012",
    "* * * * 11,12 Mon-WeD 2012",
    "* * * * * L 2012",
    "* * * * * 5L 2012",
    "* * * * 10 5L 2012",
    "* * * * 10-11 5L 2012",
    "* * * * 10,11 5L 2012-2014",
    // 测试日
    "* * * 23 * ? ",
    "* * * L * ? ",
    "* * * LW * ? ",
    "* * * 1/3 * ? ",
    "* * * 1/3 10 ? ",
    // 时分秒

    "0 0 2 1 * ? 2021",
    "0 15 10 ? * 2-6",
    "0 0 10,14,16 * * ?",
    "0 5 14-16 * * ?",
    "0 15 10 L * ?",
    "0 15 10 ? * 6",
    "0 0 12 * * ? 2002",
    "0 15 10 ? * ? 2002-2005",
    "0 15 10 ? * 6L",
    "0 15 10 6#4 * ?",
    "0 15 10 * * ? *",
    "0 15 10 * * ? 2005",
    "0 * 14 * * ?",
    "0 0/5 14 * * ?",
    "0 0/5 14,18 * * ?",
    "8/10 0/5 3/4 * * ?",
    "* * * * * *",
    "0 * * * * ?",
    "32 * * * *",
  ];

  testCases.forEach((cron) => {
    console.log(`Cron表达式: ${cron}`);
    console.log(`中文描述: ${cronToChinese(cron)}`);
    console.log("-".repeat(50));
  });
}

testCronExpression();
